void main(List<String> args) {
  // 4 types of operator

  // unary prefix , unary postfix ,binary infix and compound assignment

  // unary prefix

  var age = 30;
  print(--age); //29
  print(!true);
  print(!false);
  print(++age); // 30

  // unary bitwise complected prefix operator

  print(~1);
}
