void main(List<String> arguments) {
  // usually operates are simple functions created by the language creators and we could just used them

  const age1 = 64;
  const age2 = 30;

// plus operator

  print(age1 + age2);
  print(age1 - age2);
  print(age1 * age2);
  print(age1 / age2); // it's return double value
  print(age1 ~/ age2); // Truncating division operator.
  // do not need to use on int it could be use on two double values
  // num is super type of int and double and both are sub type of num
}
